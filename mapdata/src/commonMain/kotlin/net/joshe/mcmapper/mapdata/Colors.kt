package net.joshe.mcmapper.mapdata

data class BaseColor(val r: Int, val g: Int, val b: Int, val a: Int = 0xff) {
    private val multipliers = arrayOf(0xb4, 0xdc, 0xff, 0x87)
    private val divisor = 0xff

    fun expand() : Iterable<Byte> {
        val res = ByteArray(4 * multipliers.size)
        multipliers.forEachIndexed { idx, mul ->
            res[(idx*4)+0] = (r * mul / divisor).toUByte().toByte()
            res[(idx*4)+1] = (g * mul / divisor).toUByte().toByte()
            res[(idx*4)+2] = (b * mul / divisor).toUByte().toByte()
            res[(idx*4)+3] = a.toUByte().toByte()
        }
        return res.asIterable()
    }
}

